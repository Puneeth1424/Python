# An integer, or int, is a whole number. 

year = 2023
age = 32

# A floating-point number, or a float, is a decimal number.

pi = 3.14159
meal_cost = 12.99

# A string, or str, is used for storing text. Strings are wrapped in double quotes " " or single quotes ' '.

message = "good nite"
username = '@snoopdogg'

# A Boolean data type, or bool, stores a value that can only be either true or false.

late_to_class = False
cranky = True


